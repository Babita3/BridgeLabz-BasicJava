package Day2_problems;
import java.util.Scanner;
public class NaturalNum {

    public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);
            int var = sc.nextInt();
            int i = 0;

            System.out.println("N: ");

            while(i < var){
                i++;
                System.out.println("+"+i);
            }
        }
    }


